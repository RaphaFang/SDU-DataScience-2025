# B.2
# 逆否證 – 不等式
# 命題：若實數 x > 2 ⇒ 1/x < 0.5。
@pytest.mark.parametrize('x', [0.5, 1, 1.5, 2, 10])
def test_b1(x):
    if 1 / x >= 0.5:
        assert x <= 2
